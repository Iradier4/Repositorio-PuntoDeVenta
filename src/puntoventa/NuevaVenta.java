    private void btnNCompletarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNCompletarVentaActionPerformed
        int folio = Integer.parseInt(txtNFolioVenta.getText());
        double subtotal = Double.parseDouble(txtNSubtotal.getText());
        double iva = Double.parseDouble(txtNIVA.getText());
        double total = Double.parseDouble(txtNTotal.getText());
        
        try{
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("UPDATE Venta SET subtotal=?, iva=?, total=? WHERE folioVenta=?");
            ps.setDouble(1, subtotal);
            ps.setDouble(2, iva);
            ps.setDouble(3, total);
            ps.setInt(4, folio);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Venta realizada");
            txtNFolioVenta.setText("");
            txtNSubtotal.setText("");
            txtNIVA.setText("");
            txtNTotal.setText("");
            txtNPrecio.setText("");
            txtNCantidad.setText("");
            this.dispose();
            Sistema sis = new Sistema();
            sis.setVisible(true);
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_btnNCompletarVentaActionPerformed

