    private void cargarTablaNuevaVenta(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tbNuevaVenta.getModel();
        modeloTabla.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int folio = Integer.parseInt(txtNFolioVenta.getText());
        int columnas;
        int[] anchos={70,70,100,100,100};
        for (int i=0;i<tbNuevaVenta.getColumnCount();i++){
            tbNuevaVenta.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        try{
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT folioVenta_id, producto_id, precio, cantidad, importe FROM detalleVenta WHERE folioVenta_id=?");
            ps.setInt(1, folio);
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while (rs.next()){
                Object[] fila = new Object[columnas];
                for (int i = 0; i<columnas; i++){
                    fila[i] = rs.getObject(i + 1);
                }
                modeloTabla.addRow(fila);
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
    private void agregarVenta(){
        LocalDate todaysDate = LocalDate.now();
        int sucursalid = Integer.parseInt((txtNSucursal.getSelectedItem().toString()));
        String fecha = todaysDate.toString();
        
        try{
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareCall("{call Insertar_Venta(?,?)}");
            ps.setInt(1, sucursalid);
            ps.setString(2, fecha);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Registro de venta generado");
            
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Registro de venta no generado por el siguiente motivo");
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
    private void agregar(){
       try{
            String nombre=(txtNProducto.getSelectedItem().toString());
            ResultSet rs;
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("SELECT id,precio FROM Producto WHERE nombre=?");
            ps.setString(1, nombre);
            rs = ps.executeQuery();
            while(rs.next()){
                txtNPrecio.setText(rs.getString("precio"));
                txtNProd.setText(rs.getString("id"));
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        } 
    }
    
    private void sumaSubtotal(){
        try{
            int folio = Integer.parseInt(txtNFolioVenta.getText());
            ResultSet rs;
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareCall("SELECT SUM(importe) AS importe FROM detalleVenta WHERE folioVenta_id=?");
            ps.setInt(1, folio);
            rs = ps.executeQuery();
            while (rs.next()){
                txtNSubtotal.setText(rs.getString("importe"));
            }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
